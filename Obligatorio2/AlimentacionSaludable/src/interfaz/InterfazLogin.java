    private void btn_loginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_loginActionPerformed

        loader.show();
        login.hide();

        // lets add timeout
        new java.util.Timer().schedule(new TimerTask() {
            @Override
            public void run() {

                loader.hide();
                GestionLogin gestionLogin = new GestionLogin(repositorio);

                String usuarioRegistrado[] = gestionLogin.manejoDeDatos(
                        txt_nombreUsuario.getText(),
                        txt_pwd.getText());
                if (usuarioRegistrado[0].equals("No existe")) {
                    JOptionPane optionPane = new JOptionPane("Usuario no Registrado",
                            JOptionPane.ERROR_MESSAGE);
                    JDialog dialog = optionPane.createDialog("Login invalido");
                    dialog.setAlwaysOnTop(true);
                    dialog.setVisible(true);
                } else {
                    
                    String tipoUsuario = usuarioRegistrado[1];
                    switch (tipoUsuario) {
                        case "Usuario":
                            {
                                InterfazLogin.this.setVisible(false);
                                
                                PanelHomeDeUsuario nuevaBotonera = new PanelHomeDeUsuario(repositorio, ventana,usuarioRegistrado[2]);
                                nuevaBotonera.pack();
                                nuevaBotonera.setVisible(true);
                                break;
                            }
                        case "Profesional":
                            {
                                InterfazLogin.this.setVisible(false);
                                PanelHomeDeProfesional nuevaBotonera
                                        = new PanelHomeDeProfesional(repositorio, ventana,usuarioRegistrado[2]);
                                nuevaBotonera.pack();
                                nuevaBotonera.setVisible(true);
                                break;
                            }
                        case "Root":
                            {
                                InterfazLogin.this.setVisible(false);
                                PanelHomeDeAdministrador nuevaBotonera
                                        = new PanelHomeDeAdministrador(repositorio, ventana);
                                nuevaBotonera.pack();
                                nuevaBotonera.setVisible(true);
                                break;
                            }
                        default:
                            JOptionPane optionPane = new JOptionPane("Contrase√±a incorrecta",
                                    JOptionPane.ERROR_MESSAGE);
                            JDialog dialog = optionPane.createDialog("Login invalido");
                            dialog.setAlwaysOnTop(true);
                            dialog.setVisible(true);
                            break;
                    }
                }

            }

        }, 1000 * 2);

    }//GEN-LAST:event_btn_loginActionPerformed

